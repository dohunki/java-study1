package application;

public class ControlCharacter {

	public static void main(String[] args) {
		// 제어 문자 : \n : 한 줄 띄움,   \t : 한 번 탭
		System.out.println("=======================");
		System.out.println("\t탭\t탭만큼 띄웁니다.");
		System.out.println("=======================");
		System.out.println("자바를\n배우고\n있습니다.");

		System.out.print("이것은");
		System.out.print("공백없이");
		System.out.print("줄을");
		System.out.print("안내립니다.");
	}

}
