package list_Interface_3;

import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;

public class App {

	public static void main(String[] args) {
	// 리스트 인터페이스		
		// 어레이 리스트
		List<Integer> list1= new ArrayList<>();  // 상속
		list1.add(5);
		list1.add(7);
		list1.add(8);
		// 링크드 리스트
		List<Integer> list2= new LinkedList<>();  // 상속
		list2.add(1);
		list2.add(2);
		list2.add(3);
		
		displayList(list1); // 콘솔에 입력된 리스트타입 객체의 아이템들을 출력
		System.out.println();
		displayList(list2);
		System.out.println();
		
		List<Integer> list3= new ArrayList<>();
		list3.addAll(list1);
		list3.addAll(list2);
		displayList(list3);
	}
	/**
	 * 메서드 설명문 주석, 메서드 위에 커서를 올리면 설명문이 뜬다.
	 * 입력된 리스트타입 객체의 아이템들을 출력
	 * @param list
	 */
	private static void displayList(List<Integer> list) {
		// TODO Auto-generated method stub
		// 입력된 리스트타입 객체의 아이템들을 출력
//		list.forEach(System.out::println); // 아래와 동일한 결과 출력
		for(int i : list) {
			System.out.println(i);
		}
	}

}
